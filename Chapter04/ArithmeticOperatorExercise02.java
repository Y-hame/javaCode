public class ArithmeticOperatorExercise02 {
    public static void main(String[] args) {
        // 循环次数为4次
        for (int i = 0; i < 4; i++) {
            System.out.println("Before increment: " + i); // 输出自增前的 i
									// 输出结果         
									// Before increment: 0
									// Before increment: 1
									// Before increment: 2
									// Before increment: 3
        }
    }
}
